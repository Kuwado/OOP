package hust.NFT.datatype;

public class TopTag {
	public int id;
	public String tag;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTag() {
		return tag;
	}
	public void setTag(String tag) {
		this.tag = tag;
	}
	
	public TopTag(int id, String tag) {
		super();
		this.id = id;
		this.tag = tag;
	}
	
	
	
	
	
}
